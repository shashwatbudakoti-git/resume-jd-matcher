#Support Functions

def clean_text(text: str) -> str:
    if not text:
        return ""# No msg has been added in order to make sure the msg does not end up in extracted words list
    text = text.lower()
    text = re.sub(r'[^a-zA-Z0-9\s]', '', text)
    text = re.sub(r'\s+', ' ', text).strip()
    return text

def extract_pdf_text(pdf_file):# Extracting all texts from PDF
    text = ""
    with pdfplumber.open(pdf_file) as pdf:
        for page in pdf.pages:
            page_text = page.extract_text()
            if page_text:
                text += page_text + " "
    return text.strip()


