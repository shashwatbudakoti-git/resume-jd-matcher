def skill_gap_analysis(jd_text: str, resume_text: str): # Extraction and comparision
    jd_keywords = set(extract_keywords(jd_text, top_n=30))
    resume_keywords = set(extract_keywords(resume_text, top_n=30))
    present = list(resume_keywords.intersection(jd_keywords))
    missing = list(jd_keywords.difference(resume_keywords))
    return present, missing

