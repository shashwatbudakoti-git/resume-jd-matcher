class ResumeJDMatcher:
    def __init__(self):
        self.model = None
        self.resume_embeddings = None
        self.resume_texts = None

    def load_model(self):
        self.model = SentenceTransformer('all-mpnet-base-v2')
        return self.model

    def encode(self, texts):
        if isinstance(texts, str):
            texts = [texts]
        return self.model.encode(texts, convert_to_numpy=True)

    def compute_similarity(self, emb1, emb2):
        return float(np.dot(emb1, emb2) / (norm(emb1) * norm(emb2)))

    def save_embeddings(self, resume_texts, filepath="resume_embeddings.pkl"):
        if self.model is None:
            self.load_model()
        self.resume_texts = resume_texts
        self.resume_embeddings = self.encode(resume_texts)
        with open(filepath, 'wb') as f:
            pickle.dump({'texts': resume_texts, 'embeddings': self.resume_embeddings}, f)
        print(f"Saved {len(resume_texts)} resume embeddings.")

    def load_embeddings(self, filepath="resume_embeddings.pkl"):
        if os.path.exists(filepath):
            with open(filepath, 'rb') as f:
                data = pickle.load(f)
            self.resume_texts = data['texts']
            self.resume_embeddings = data['embeddings']
            return True
        return False

    def match_jd(self, jd_text, top_k=5):
        if self.model is None:
            self.load_model()
        if self.resume_embeddings is None:
            raise ValueError("No resume embeddings loaded.")
        jd_embedding = self.encode(jd_text)[0]
        similarities = [(i, self.compute_similarity(jd_embedding, emb)) for i, emb in enumerate(self.resume_embeddings)]
        similarities.sort(key=lambda x: x[1], reverse=True)
        return [{'rank': i+1, 'match_score': round(score*100, 2), 'resume_preview': self.resume_texts[idx][:200] + "..."}
                for i, (idx, score) in enumerate(similarities[:top_k])]


