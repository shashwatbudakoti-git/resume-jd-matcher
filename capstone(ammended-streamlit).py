def replace_synonyms(text: str) -> str:
    if not text:
        return ""
    text_lower = text.lower()
    for synonym in sorted(SYNONYM_TO_CANONICAL.keys(), key=len, reverse=True):
        if synonym in text_lower:
            text_lower = re.sub(r'\b' + re.escape(synonym) + r'\b', SYNONYM_TO_CANONICAL[synonym], text_lower)
    return text_lower


def clean_text(text: str) -> str:
    if not text:
        return ""
    text = text.lower()
    text = re.sub(r'[^a-zA-Z0-9\s]', '', text)
    text = re.sub(r'\s+', ' ', text).strip()
    return replace_synonyms(text)


def extract_pdf_text(pdf_file) -> str:
    text = ""
    with pdfplumber.open(pdf_file) as pdf:
        for page in pdf.pages:
            page_text = page.extract_text()
            if page_text:
                text += page_text + " "
    return text.strip()


def extract_keywords(text: str, top_n: int = 20) -> List[str]:
    stop_words = {
        'a', 'an', 'and', 'are', 'as', 'at', 'be', 'by', 'for', 'from',
        'has', 'he', 'in', 'is', 'it', 'its', 'of', 'on', 'that', 'the',
        'to', 'was', 'were', 'will', 'with', 'i', 'you', 'we', 'they',
        'this', 'that', 'these', 'those', 'am', 'do', 'does', 'did',
        'have', 'had', 'having', 'been', 'being', 'would', 'could',
        'should', 'might', 'must', 'my', 'your', 'his', 'her', 'our',
        'their', 'me', 'him', 'us', 'them'
    }
    words = clean_text(text).split()
    keywords = [w for w in words if w not in stop_words and (len(w) > 2 or w.isdigit())]
    freq = Counter(keywords)
    return [word for word, _ in freq.most_common(top_n)]


def skill_gap_analysis(jd_text: str, resume_text: str) -> Tuple[List[str], List[str]]:
    jd_keywords = set(extract_keywords(jd_text, top_n=30))
    resume_keywords = set(extract_keywords(resume_text, top_n=30))
    return list(resume_keywords.intersection(jd_keywords)), list(jd_keywords.difference(resume_keywords))


