print(f"SBERT Average Match Score: {np.mean(sbert_scores):.2f}%")
print(f"Score Range: {min(sbert_scores):.2f}% - {max(sbert_scores):.2f}%")

# Combine JD and Resume embeddings
concat_embeddings = np.concatenate([jd_embeddings, resume_embeddings], axis=1)
print(f"Concatenated embeddings shape: {concat_embeddings.shape}")

# Split with stratification
X_train, X_test, y_train, y_test = train_test_split( concat_embeddings, labels, test_size=0.2, random_state=42, stratify=labels)
print(f"Training samples: {len(X_train)}")
print(f"Test samples: {len(X_test)}")

# Train classifier
print("\nTraining Logistic Regression...")
clf = LogisticRegression(max_iter=1000)
clf.fit(X_train, y_train)

# Evaluate
y_pred = clf.predict(X_test)
sbert_acc = accuracy_score(y_test, y_pred)

print(f"SBERT Classification Accuracy: {sbert_acc:.2%}")
print("\nClassification Report:")
print(classification_report(y_test, y_pred))

