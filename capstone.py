class ResumeJDMatcher:
    def __init__(self):
        self.model = None
        self.resume_embeddings = None
        self.resume_texts = None

    def load_model(self):
        self.model = SentenceTransformer('all-mpnet-base-v2')#all-MiniLM-L6-v2(384 dimensional embedding size is optimum and fast). But for greater accuracy we are using all-mpnet-base-v2 768 dimensional embedding. We can also consider distiluse-base-multilingual (512 dimensional embedding size) or m2v-256-distiluse-base-multilingual-cased-v2 (256 dimensional embedding size)) .
        print("Model loaded.")
        return self.model

    def encode(self, texts):
        if isinstance(texts, str):
            texts = [texts]
        return self.model.encode(texts, convert_to_numpy=True)#Vectors created(Embedding)

    def compute_similarity(self, emb1, emb2):
        from numpy.linalg import norm
        return float(np.dot(emb1, emb2) / (norm(emb1) * norm(emb2)))#Cosine Singlularity

    def save_embeddings(self, resume_texts, filepath="resume_embeddings.pkl"):#Saving Embedding PErmanently
        if self.model is None:
            self.load_model()
        self.resume_texts = resume_texts
        self.resume_embeddings = self.encode(resume_texts)
        with open(filepath, 'wb') as f:
            pickle.dump({'texts': resume_texts, 'embeddings': self.resume_embeddings}, f)
        print(f"Saved {len(resume_texts)} resume embeddings.")

    def load_embeddings(self, filepath="resume_embeddings.pkl"):#Second most important part.Will reduce time each resume is processed
        if os.path.exists(filepath):
            with open(filepath, 'rb') as f:
                data = pickle.load(f)
            self.resume_texts = data['texts']
            self.resume_embeddings = data['embeddings']
            print(f"Loaded {len(self.resume_texts)} resume embeddings.")
            return True
        return False

    def match_jd(self, jd_text, top_k=5):# the Actual function (actual matching process). By default only Top 5 candidates/resumes will be found. Can be changed
        if self.model is None:
            self.load_model()
        if self.resume_embeddings is None:
            raise ValueError("No resume embeddings loaded. Run save_embeddings() first.")# Safety to prevent crashes

        jd_embedding = self.encode(jd_text)[0]# JD Embedding
        # Compare with all resumes
        similarities = []
        for i, resume_emb in enumerate(self.resume_embeddings):
            score = self.compute_similarity(jd_embedding, resume_emb)
            similarities.append((i, score))# Store resume index and its score

        similarities.sort(key=lambda x: x[1], reverse=True)# Sort by score (highest first)

        results = [] # Build results
        for idx, score in similarities[:top_k]:#Variable (top 5)
            results.append({
                'rank': len(results) + 1,
                'match_score': round(score * 100, 2),
                'resume_preview': self.resume_texts[idx][:200] + "..."
            })
        return results

