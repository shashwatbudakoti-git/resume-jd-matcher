def replace_synonyms(text: str) -> str:
    if not text:
        return ""

    text_lower = text.lower()

    sorted_synonyms = sorted(SYNONYM_TO_CANONICAL.keys(), key=len, reverse=True)# Sort by length (longest first) to prevent partial matches. e.g., "java standard edition" should match before "java"

    for synonym in sorted_synonyms:
        if synonym in text_lower:# Replace whole word only (using word boundaries)
            import re
            pattern = r'\b' + re.escape(synonym) + r'\b'
            text_lower = re.sub(pattern, SYNONYM_TO_CANONICAL[synonym], text_lower)

    return text_lower


def clean_text(text: str) -> str:
    if not text:
        return ""# No msg has been added in order to make sure the msg does not end up in extracted words list
    text = text.lower()
    text = re.sub(r'[^a-zA-Z0-9\s]', '', text)
    text = re.sub(r'\s+', ' ', text).strip()
    text = replace_synonyms(text)
    return text

def extract_pdf_text(pdf_file):# Extracting all texts from PDF
    text = ""
    with pdfplumber.open(pdf_file) as pdf:
        for page in pdf.pages:
            page_text = page.extract_text()
            if page_text:
                text += page_text + " "
    return text.strip()


def extract_keywords(text: str, top_n: int = 20) -> list:
    # Hardcoded stopwords (no NLTK needed).Streamlit may create problems.
    stop_words = {
        'a', 'an', 'and', 'are', 'as', 'at', 'be', 'by', 'for', 'from',
        'has', 'he', 'in', 'is', 'it', 'its', 'of', 'on', 'that', 'the',
        'to', 'was', 'were', 'will', 'with', 'i', 'you', 'we', 'they',
        'this', 'that', 'these', 'those', 'am', 'do', 'does', 'did',
        'have', 'had', 'having', 'been', 'being', 'would', 'could',
        'should', 'might', 'must', 'my', 'your', 'his', 'her', 'our',
        'their', 'me', 'him', 'us', 'them'
    }

    words = clean_text(text).split()

    keywords = []
    for w in words:
        if w not in stop_words and (len(w) > 2 or w.isdigit()):
            keywords.append(w)

    freq = Counter(keywords)

    return [word for word, _ in freq.most_common(top_n)]


def skill_gap_analysis(jd_text: str, resume_text: str): # Extraction and comparision
    jd_keywords = set(extract_keywords(jd_text, top_n=30))
    resume_keywords = set(extract_keywords(resume_text, top_n=30))
    present = list(resume_keywords.intersection(jd_keywords))
    missing = list(jd_keywords.difference(resume_keywords))
    return present, missing

